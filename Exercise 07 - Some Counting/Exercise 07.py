# Exercise 07 - Some Counting

# 1. Count up from 0 to 50 in increments of 1
print("Counting up from 0 to 50:")
for i in range(0, 51, 1):
    print(i)

# 2. Count down from 50 to 0 in decrements of 1
print("\nCounting down from 50 to 0:")
for i in range(50, -1, -1):
    print(i)

# 3. Count up from 30 to 50 in increments of 1
print("\nCounting up from 30 to 50:")
for i in range(30, 51, 1):
    print(i)

# 4. Count down from 50 to 10 in increments of 2
print("\nCounting down from 50 to 10 by 2s:")
for i in range(50, 9, -2):
    print(i)

# 5. Count up from 100 to 200 in increments of 5
print("\nCounting up from 100 to 200 by 5s:")
for i in range(100, 201, 5):
    print(i)
